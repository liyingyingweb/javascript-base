// 逻辑运算符
//&& 并且
var a = true;
var b = false;
var result = a&&b;
console.log(result);   //false

//  || 或者
var a = false;
var b = true;
var result = a||b;
console.log(result);   //true 

// ! 取反
var a = false;
var b = false;
var result = !a;
console.log(result);   //true 

var a = true;
var b = true;
var result = !a;
console.log(result);   //false 
